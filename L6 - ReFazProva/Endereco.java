public final class Endereco{
    private String rua = " ";
    private int num = 0;

    public void setNum(int num) {
        this.num = num;
    }
    
    public void setRua(String rua) {
        this.rua = rua;
    }

    public int getNum() {
        return num;
    }

    public String getRua() {
        return rua;
    }
}